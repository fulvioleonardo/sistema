        components: {SaleNotesFormItem, PersonForm, SaleNotesOptions, Logo},
        data() {
            return {
                resource: 'sale-notes',
                showDialogAddItem: false,
                showDialogNewPerson: false,
                showDialogOptions: false,
                loading_submit: false,
